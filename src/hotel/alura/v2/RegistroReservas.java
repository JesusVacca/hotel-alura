    private void btn_siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_siguienteActionPerformed
        // TODO add your handling code here:
        Scanner lector = null;
        File fl = new File("Datos-usuario.txt");
        if(fl.exists()){
            try {
                lector = new Scanner(fl);
                while (lector.hasNextLine()) {                    
                    String data = lector.nextLine().split(",")[0];
                    cedula = Long.valueOf(data);
                }
                lector.close();
            } catch (FileNotFoundException ex) {
                throw new RuntimeException(ex);
            }
        }
        if(CamposTexto()){
            try {
                // Codigo de ejecucion.
                this.reservas = new Reservas();
                this.reservas.setDni_cliente(cedula);
                this.reservas.setFecha_ingreso( this.fecha_inicio.getDate());
                this.reservas.setFecha_salida( this.fecha_salida.getDate());
                this.reservas.setValor(this.valor_apagar);
                
                this.reservas.setId_forma_pago(
                        this.datos_formas_pagos.get(
                                this.forma_pago.getSelectedItem()
                        )
                );
                this.con = ConnectionFactory.getConnection(
                        "hotel_alura",
                        "root",
                        "Jesus@1999"
                );
                this.connectionFactory.registerReserva(reservas,con);
            } catch (SQLException ex) {
                throw new RuntimeException(ex);
            }
        }
        
    }//GEN-LAST:event_btn_siguienteActionPerformed

    private void btn_generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_generarActionPerformed
        // TODO add your handling code here:
        if(this.fecha_inicio.getDate() != null && this.fecha_salida.getDate() != null){
            int diaInicio = this.fecha_inicio.getDate().getDate();
            int diaFin = this.fecha_salida.getDate().getDate();
            String info = "";
            if(diaFin>diaInicio) {
                this.valor_apagar = (diaFin-diaInicio+1)*this.tarifaDia;
                info = "$ "+this.valor_apagar;
            }
            else if(diaFin==diaInicio){
                this.valor_apagar = this.tarifaDia;
                info = "$ "+this.valor_apagar;
            }
            this.valor_reserva.setText(info);
        }
    }//GEN-LAST:event_btn_generarActionPerformed

