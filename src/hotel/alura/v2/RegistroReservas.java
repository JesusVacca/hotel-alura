    private void loadFormas(JComboBox<String> forma_pago) {
        try {
            this.con = ConnectionFactory.getConnection(
                    "hotel_alura",
                    "root",
                    "Jesus@1999"
            );
            this.connectionFactory = new ConnectionFactory();
            this.datos_formas_pagos = this.connectionFactory.loadFormas(con);
            
            for(Map.Entry<String,Integer> item: datos_formas_pagos.entrySet()){
                forma_pago.addItem(item.getKey());
            }
            
            
        } catch (SQLException ex) {
            throw new RuntimeException();
        }
    }

    private boolean CamposTexto() {
        boolean validar = true;
        String data = 
                this.fecha_inicio.getDate()+","+
                this.fecha_salida.getDate()+","+
                this.valor_reserva.getText();
        for(String item:data.split(",")){
            if(item == null){
                validar=false;
                break;
            }
        }
        return validar;
    }
